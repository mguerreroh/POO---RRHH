    public void limpiaCampos(){
        int fil;
        int col;
        
        txtNombre.setText("");
        edtDescripcion.setText("");
        
        fil = grdPermisos.getRowCount();
        col = grdPermisos.getColumnCount();
        
        for(int fila = 0; fila < fil; fila++){
            for(int colu = 1; colu < col -1; colu++){
                grdPermisos.setValueAt(false, fila, colu);
            }
        }
        txtNombre.grabFocus();
    }

