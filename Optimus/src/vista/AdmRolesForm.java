    private void cmdAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAdicionarActionPerformed
        String nombre;
        String descripcion;
        String[][] aMatriz = new String[5][5];
        
        int fil;
        int col;
        int filas;
        int colum;
        
        filas = grdPermisos.getColumnCount();
        colum = grdPermisos.getRowCount();
        
        for(fil=0; fil < filas; fil++){
            for(col=1; col<colum;col++){
                aMatriz[fil][col-1] = grdPermisos.getValueAt(fil, col).toString();
            }
        }
        
        
        nombre = txtNombre.getText();
        descripcion = edtDescripcion.getText();
//        aModuloXRol.add(new ModuloXRol(descripcion, nombre));
        
        
        limpiaCampos();
    }//GEN-LAST:event_cmdAdicionarActionPerformed

